package by.bsuir.system.service.exception;

public class GiftCertificateServiceException
        extends ServiceException {

    public GiftCertificateServiceException() {
    }

    public GiftCertificateServiceException(String message) {
        super(message);
    }

    public GiftCertificateServiceException(Throwable cause) {
        super(cause);
    }

    public GiftCertificateServiceException(String message, Throwable cause) {
        super(message, cause);
    }

    public GiftCertificateServiceException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
            
}
